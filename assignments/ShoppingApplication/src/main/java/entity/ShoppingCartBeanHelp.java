package entity;

public class ShoppingCartBeanHelp {
    private String id;
    private Integer number;

    public ShoppingCartBeanHelp(String id, Integer number) {
        this.id = id;
        this.number = number;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }
}
